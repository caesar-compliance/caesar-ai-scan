/**
 * Generates a compliance-reviewer friendly Markdown report for the export pack.
 *
 * @param {Object} exportPack - The full CaesarEvidenceExportPack parent object.
 * @returns {string} The Markdown content of REVIEW_SUMMARY.md.
 */
export function generateExportPackReport(exportPack) {
  const {
    pack_id,
    generated_at,
    target_project,
    source_tool_version,
    manifest,
    import_readiness,
    human_review_checklist
  } = exportPack;

  const blockers = import_readiness.blockers || [];
  const warnings = import_readiness.warnings || [];
  const lanes = human_review_checklist.review_lanes || [];
  const questions = human_review_checklist.required_questions || [];
  const confirmations = human_review_checklist.required_confirmations || [];
  const safety = manifest.safety_flags || {};

  let markdown = `# Caesar AI Compliance — Evidence Export Pack Review Summary

This self-contained local package compiles the AI code scanning findings, candidate evidence drafts, and automated readiness evaluations for compliance auditing.

---

## 📋 General Metadata

| Parameter | Value |
| :--- | :--- |
| **Pack ID** | \`${pack_id}\` |
| **Generated At** | \`${generated_at}\` |
| **Target Project** | \`${target_project}\` |
| **Scanner Version** | \`caesar-ai-scan v${source_tool_version}\` |
| **Manifest ID** | \`${manifest.manifest_id}\` |

---

## 🚦 Import Readiness & Safety Assessment

| Assessment | Status | Value / Detail |
| :--- | :--- | :--- |
| **Readiness Status** | ${import_readiness.readiness_status === 'evidence_candidate_ready' ? '🟢 **READY**' : import_readiness.readiness_status === 'needs_human_review' ? '🟡 **NEEDS HUMAN REVIEW**' : '🔴 **BLOCKED**'} | \`${import_readiness.readiness_status}\` |
| **Readiness Score** | 📈 **${import_readiness.readiness_score}/100** | Compliance metric based on unresolved items |
| **Automated Import** | 🚫 **LOCKED** | \`can_import_automatically: false\` (Hard-coded safety constraint) |
| **Sign-off Required** | ✍️ **REQUIRED** | \`signoff_required: true\` |

### 🔒 Safety Flags

*   **No Plaintext Credentials Exposed:** ${safety.no_plaintext_credentials_exposed ? '✅ PASS' : '❌ FAIL (Env variables flagged)'}
*   **Hardlocked to Draft Status:** ${safety.all_candidates_are_drafts ? '✅ PASS' : '⚠️ WARNING (Non-draft items detected)'}
*   **No Competitor Licenses Flagged:** ${safety.no_competitor_licenses_flagged ? '✅ PASS' : '❌ FAIL (Competitor license patterns present)'}

### 💡 Recommended Next Action
> **${import_readiness.recommended_next_action}**

---

`;

  if (blockers.length > 0) {
    markdown += `## ❌ Blocking Issues (${blockers.length})

The following items are blockers preventing direct or automated ingestion:
${blockers.map(b => `- [ ] ${b}`).join('\n')}

---

`;
  }

  if (warnings.length > 0) {
    markdown += `## ⚠️ Non-Blocking Warnings (${warnings.length})

Please review these warnings:
${warnings.map(w => `- ${w}`).join('\n')}

---

`;
  }

  markdown += `## 🚏 Required Human Review Lanes (${lanes.length})

Compliance validation tasks must be routed through the following auditor workflows:
${lanes.map(l => `- **${l.toUpperCase()} Lane**: Check compliance and confirm policies.`).join('\n')}

---

## ❓ Auditor Assessment Questions

The reviewer should investigate and answer the following questions:
${questions.map((q, idx) => `${idx + 1}. ${q}`).join('\n')}

---

## ✍️ Manual Auditor Sign-off Checklist

An auditor must manually confirm the following constraints before importing these candidates into **Governance OS** or **Caesar AI Evidence**:

${confirmations.map(c => `- [ ] ${c}`).join('\n')}

---

*Generated by Caesar AI Governance Ecosystem scanner. For offline verification only.*
`;

  return markdown;
}
